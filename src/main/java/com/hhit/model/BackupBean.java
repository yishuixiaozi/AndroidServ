package com.hhit.model;

public class BackupBean {
    int backupid;
    String backupusername;
    String backuptime;
    String backfilename;
    String backfilepath;

    public int getBackupid() {
        return backupid;
    }

    public void setBackupid(int backupid) {
        this.backupid = backupid;
    }

    public String getBackupusername() {
        return backupusername;
    }

    public void setBackupusername(String backupusername) {
        this.backupusername = backupusername;
    }

    public String getBackuptime() {
        return backuptime;
    }

    public void setBackuptime(String backuptime) {
        this.backuptime = backuptime;
    }

    public String getBackfilename() {
        return backfilename;
    }

    public void setBackfilename(String backfilename) {
        this.backfilename = backfilename;
    }

    public String getBackfilepath() {
        return backfilepath;
    }

    public void setBackfilepath(String backfilepath) {
        this.backfilepath = backfilepath;
    }
}
